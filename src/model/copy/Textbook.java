package model.copy;

public class Textbook {
	
	private String title;
	private String ISBN;
	
	public Textbook() {
		this.title = "";
		this.ISBN = "";
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getISBN() {
		return ISBN;
	}

	public void setISBN(String iSBN) {
		ISBN = iSBN;
	}
	
	

}
